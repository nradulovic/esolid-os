void parseWordToBytes(uint32_t aWord, volatile uint8_t * pStoreBuffer) {

    pStoreBuffer[0] = ((uint8_t)(aWord >> 24));
    pStoreBuffer[1] = (uint8_t)((aWord << 8) >> (uint32_t)24);
    pStoreBuffer[2] = (uint8_t)((aWord << 16) >> (uint32_t)24);
    pStoreBuffer[3] = (uint8_t)(aWord);
}


