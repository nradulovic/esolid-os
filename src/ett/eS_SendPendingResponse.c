/*************************************************************************************************
 * GLOBAL FUNCTION DEFINITIONS
 *************************************************************************************************/

uint32_t eS_SendPendingResponse(void) {

    if(responseIndex < (uint8_t)0x7) {                                          /* Ovaj if zajedno sa interaptom koji poziva ovu f-ju cini for petlju koja iscitava bafer koji sadrzi ACK ili NACK */
        HAL_USART_SEND(HAL_USART1, pEppResponseFrame[responseIndex]);
        responseIndex++;
    } else {
        responseIndex = (uint8_t)0x0;
        pEppResponseFrame = (void *)0;
    }
    return ((uint32_t)(pEppResponseFrame > (uint8_t *)0 ? 100 : 0));
}


