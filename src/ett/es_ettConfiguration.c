void eS_ettConfiguration(void) {

    primaryRxBufferFilled = (uint16_t)0x0;
    pEppResponseFrame = (void *)0;
    stpRdIndex = (uint32_t)0x0;
    stpWrIndex = (uint32_t)0x0;

    SMM_MEMORY_DEFINE(100);

    HAL_SYSTICK_CLOCK_CONFIG(SysTick_CLKSource_HCLK);
    HAL_SYSTICK_CONFIG(24000);                                                  /* Konfiguracija systemtick timera na 1mS */

    HAL_USART_INIT(
                  HAL_USART1,                                                   /* Usart 2 */
                  4800,                                                         /* Baudrate je 9600 */
                  HAL_USART_INOUT,                                              /* Konfigurisan za RX i TX */
                  USART_WordLength_8b,                                          /* Duzina reci * bita */
                  USART_StopBits_1,                                             /* Jedan ETT_STOP bit */
                  USART_Parity_No,                                              /* Ne koristi se PARITY provera */
                  USART_HardwareFlowControl_None);                              /* Ne koristi se hardverski HANDSHAKE */

//    HAL_USART_IT_CONFIG(HAL_USART1, USART_IT_RXNE, 21, ENABLE);                 /* Dozvoli RX interapt */
//    HAL_USART_IT_CONFIG(HAL_USART1, USART_IT_TC,  20, ENABLE);                  /* Dozvoli TX interapt */
//    HAL_USART_CMD(HAL_USART1, ENABLE);                                          /* Ukljuci USART1 */
}


