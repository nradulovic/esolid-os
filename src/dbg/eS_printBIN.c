/*-----------------------------------------------------------------------------------------------*/
/**
 * @ingroup     dbg_intf
 */
/*-----------------------------------------------------------------------------------------------*/
void eS_printBIN(
    uint32_t bin) {

    char binDigits[32];
    uint32_t i;
    uint8_t lastDigit = (uint8_t)0x0;

    for(i = (uint32_t)0; i < (uint32_t)32; i++) {
        if((uint32_t)0x1 == (bin & (uint32_t)0x1)) {
            binDigits[i] = (char)'1';
            lastDigit = (uint8_t)i;
        } else {
            binDigits[i] = (char)'0';
        }
        bin >>= (uint32_t)0x1;
    }
    eS_sendDigits(&binDigits[0], lastDigit);
}


