/**
 * @ingroup     dbg_intf
 */
/*-----------------------------------------------------------------------------------------------*/
void eS_printString(
    const char * aString) {

    while (*aString != '\0') {
        HAL_USART_SEND(HAL_USART2, (uint8_t)(*aString));
        HAL_USART_WAIT_FOR_LAST_TASK(HAL_USART2, USART_FLAG_TXE);
        ++aString;
    }
}


