bool_T eS_dbgValidUint32(
    uint32_t 	aValue,
    uint32_t 	aMin,
    uint32_t 	aMax) {

    if (aMin == aMax) {

        return (FALSE);
    } else if ((aValue > aMin) && (aValue < aMax)) {

        return (TRUE);
    } else {

        return (FALSE);
    }
}


