/*-----------------------------------------------------------------------------------------------*/
/**
 * @ingroup     dbg_intf
 */
/*-----------------------------------------------------------------------------------------------*/
bool_T eS_dbgValidString(
    const uint8_t   * aString,
    size_t 		    aSize) {

	DBG_CHECK((uint8_t *)0 != aString);
	DBG_CHECK((size_t)0 < aSize);

    while ((uint32_t)0 != aSize) {

        if (0x00 == *aString) {

            return (TRUE);                                                      /* niz aString je ispravan */
        }
        --aSize;
        ++aString;
    }

    return (FALSE);                                                             /* niz aString NIJE ispravan */
}


