void eS_printCONFIG(void) {

    signedStatus = (SignedStatus_T)DBG_UNSIGNED;

    HAL_USART_INIT(
        HAL_USART2,                                                             /* Usart 2                                                  */
        115200,                                                                 /* Baudrate je 115200                                       */
        HAL_USART_INOUT,                                                        /* Konfigurisan za RX i TX                                  */
        USART_WordLength_8b,                                                    /* Duzina reci 8 bita                                       */
        USART_StopBits_1,                                                       /* Jedan ETT_STOP bit                                       */
        USART_Parity_No,                                                        /* Ne koristi se PARITY provera                             */
        USART_HardwareFlowControl_None);                                        /* Ne koristi se hardverski HANDSHAKE                       */

    HAL_USART_CMD(HAL_USART2, ENABLE);                                          /* Ukljuci usart2                                           */
}


