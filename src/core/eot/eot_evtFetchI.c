eot_evt_T * eot_evtFetchI(
    epe_epa_T       * aEpa) {

    eot_evt_T * tmpEvt;

    EOT_DBG_CHECK((epe_epa_T *)0U != aEpa);                                     /* Provera par: da li je aEpa inicijalizovan?               */
    EOT_ASSERT(FALSE == eS_qPtrIsEmpty(&(aEpa->evtQueue.queue)));              /* Provera: da li red sadrzi barem jedan element?           */
    tmpEvt = (eot_evt_T *)eS_qPtrGetI(
        &(aEpa->evtQueue.queue));

    if (TRUE == eS_qPtrIsEmpty(&(aEpa->evtQueue.queue))) {
        epe_schedRdyRemoveI(epa);
        EPE_SCHED_NOTIFY_NRDY_I(
            aEpa);
    }

#if defined(OPT_EPA_USE_REGISTRY) || defined(OPT_DBG_EOT)
    eot_evtQStatEvtRemoved(
        &(aEpa->evtQueue));
#endif

    return (tmpEvt);
}


