void eS_evtPost(
    epe_epa_T       * aEpa,
    eot_evt_T       * aEvt) {

    HAL_CRITICAL_DECL();

    EOT_DBG_CHECK((epe_epa_T *)0U != aEpa);                                     /* Provera par: da li je aEpa inicijalizovan?               */
    EOT_DBG_CHECK((eot_evt_T *)0U != aEvt);                                     /* Provera par: da li je aEvt inicijalizovan?               */
    EOT_ASSERT(FALSE == eS_qPtrIsFull(&(aEpa->evtQueue.queue)));                /* Provera: da li red moze da primi nov element?            */

#if defined(OPT_EVT_USE_TRANSMITTER)
    aEvt->transmitter = epe_kernCurrEpaGet();
#endif
    HAL_CRITICAL_ENTER();
    ++aEvt->dyn;
    EOT_ASSERT(
        (EVT_QUALIF_MASK != aEvt->dyn) &&
        ((eot_evtDyn_T)0U != aEvt->dyn));                                       /* Provera: da li je brojac prekoracio?                     */
    eS_qPtrPutI(
        &(aEpa->evtQueue.queue),
        (void *)aEvt);
                                                                                /* Povecaj info o zauzecu reda cekanja.                     */
#if defined(OPT_EPA_USE_REGISTRY) || defined(OPT_DBG_EOT)
    eot_evtQStatEvtAdded(
        &(aEpa->evtQueue));
#endif
    epe_schedRdyInsertI(aEpa);
    EPE_SCHED_NOTIFY_RDY_I(
        aEpa);
    HAL_CRITICAL_EXIT();
}


