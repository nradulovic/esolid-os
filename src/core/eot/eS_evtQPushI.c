void eS_evtQPushI(
    eot_evtQueue_T  * aEvtQueue,
    eot_evt_T       * aEvt) {

    EOT_DBG_CHECK((eot_evtQueue_T *)0U != aEvtQueue);                           /* Provera par: da li je aEvtQueue inicijalizovan?          */
    EOT_DBG_CHECK((eot_evt_T *)0U != aEvt);                                     /* Provera par: da li je aEvt inicijalizovan?               */
    EOT_ASSERT(FALSE == eS_qPtrIsFull(&(aEvtQueue->queue)));                    /* Provera: da li red moze da primi nov element?            */

    if (EVT_QUALIF_DYNAMIC == EVT_STORAGE_RD(aEvt)) {
        ++aEvt->dyn;
    }
    eS_qPtrPushI(
        &(aEvtQueue->queue),
        (void *)aEvt);

#if defined(OPT_DBG_EOT)
    eot_evtQStatEvtAdded(
        aEvtQueue);
#endif
}


