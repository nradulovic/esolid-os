void * eS_evtPlnCreate(
    size_t      aEvtSize,
    eot_evtId_T aEvtID) {

    eot_evtPln_T * pNewEvt;

    EOT_DBG_CHECK(sizeof(eot_evtPln_T) <= aEvtSize);                            /* Provera par: da li dogadjaj ima velicinu vecu od min.?   */
    pNewEvt = (eot_evtPln_T *)OPT_MALLOC(aEvtSize);                             /* Dobavi potreban memorijski prostor.                      */
    EOT_ASSERT((eot_evtPln_T *)0 != pNewEvt);                                   /* Provera: da li je mem. prostor rezervisan za dogadjaj?   */
    pNewEvt->size = aEvtSize;
    pNewEvt->ID = aEvtID;

#if defined(OPT_EVT_USE_TYPE)                                                   /* Da li se koriste tipovi dogadjaja?                       */
    pNewEvt->type = EVT_TYPE_UNSPEC;
#endif

    return ((void *)pNewEvt);
}


