/**
 * @ingroup     epe_impl
 */
/*-----------------------------------------------------------------------------------------------*/
void eS_epaPrioSet(
    epe_epa_T       * aEpa,
    uint8_t         aNewPrio) {

    HAL_CRITICAL_DECL();
    bool_t state;

    HAL_CRITICAL_ENTER();
    state = epe_schedEpaIsRdy(
        aEpa);
    epe_schedUnRegisterI(
        aEpa);
    aEpa->kernCtrl.prio = (epe_epaPrio_T)aNewPrio;
    epe_schedRegisterI(
        aEpa);

    if (TRUE == state) {
        epe_schedRdyInsertI(
            aEpa);
    }
    HAL_CRITICAL_EXIT();
}


