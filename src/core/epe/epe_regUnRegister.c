void epe_regUnRegister(
    epe_epa_T       * aEpa) {

#if defined(OPT_EPA_USE_REGISTRY) || defined(__DOXYGEN__)

    HAL_CRITICAL_DECL();

    HAL_CRITICAL_ENTER();
    eS_dlsNodeRemoveI(
        &(aEpa->registry.epaList));
    --epe_regSentinel.activeCnt;
    HAL_CRITICAL_EXIT();
    eS_dlsNodeInitI(
        &(aEpa->registry.epaList));
#endif
}


